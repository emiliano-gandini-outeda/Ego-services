export function createPDF(content: string, title: string) {
  const script = document.createElement("script")
  script.src = "https://cdnjs.cloudflare.com/ajax/libs/jspdf/2.5.1/jspdf.umd.min.js"
  script.async = true

  script.onload = () => {
    const { jsPDF } = window.jspdf
    const doc = new jsPDF()

    // Set PDF properties
    doc.setProperties({
      title: title,
      subject: title,
      author: "Eclipse Growth Optimization Services",
      creator: "EGOS Website",
    })

    // Professional layout constants
    const pageWidth = 210 // A4 width in mm
    const pageHeight = 297 // A4 height in mm
    const margin = 20
    const contentWidth = pageWidth - 2 * margin
    const headerHeight = 25
    const footerHeight = 15

    // Professional color palette
    const colors = {
      primary: [220, 38, 38], // EGOS Red
      primaryLight: [239, 68, 68], // Lighter red
      dark: [31, 41, 55], // Dark gray
      darkLight: [55, 65, 81], // Medium gray
      text: [17, 24, 39], // Almost black
      textLight: [75, 85, 99], // Gray text
      background: [249, 250, 251], // Light gray background
      white: [255, 255, 255],
      accent: [245, 245, 245], // Very light gray
    }

    let currentPage = 1
    let yPosition = headerHeight + 15

    // Helper function to add a new page
    const addNewPage = () => {
      doc.addPage()
      currentPage++
      yPosition = headerHeight + 15
      addPageBackground()
      addHeader()
    }

    // Add professional page background
    const addPageBackground = () => {
      // White background
      doc.setFillColor(...colors.white)
      doc.rect(0, 0, pageWidth, pageHeight, "F")

      // Subtle side accent
      doc.setFillColor(...colors.accent)
      doc.rect(0, 0, 3, pageHeight, "F")
    }

    // Add professional header
    const addHeader = () => {
      // Header background with gradient effect
      doc.setFillColor(...colors.primary)
      doc.rect(0, 0, pageWidth, headerHeight, "F")

      // Company name
      doc.setTextColor(...colors.white)
      doc.setFontSize(16)
      doc.setFont("helvetica", "bold")
      doc.text("EGOS", margin, 12)

      // Document title
      doc.setFontSize(10)
      doc.setFont("helvetica", "normal")
      doc.text(title.toUpperCase(), margin, 20)

      // Decorative line
      doc.setDrawColor(...colors.primaryLight)
      doc.setLineWidth(0.5)
      doc.line(margin, headerHeight - 2, pageWidth - margin, headerHeight - 2)
    }

    // Add professional footer
    const addFooter = () => {
      const footerY = pageHeight - footerHeight

      // Footer line
      doc.setDrawColor(...colors.accent)
      doc.setLineWidth(0.3)
      doc.line(margin, footerY + 2, pageWidth - margin, footerY + 2)

      // Footer text
      doc.setTextColor(...colors.textLight)
      doc.setFontSize(8)
      doc.setFont("helvetica", "normal")

      // Left side - company info
      doc.text("Eclipse Growth Optimization Services", margin, footerY + 8)
      doc.text("ego-services.com", margin, footerY + 12)

      // Right side - page number
      const pageText = `Page ${currentPage}`
      const pageTextWidth = doc.getTextWidth(pageText)
      doc.text(pageText, pageWidth - margin - pageTextWidth, footerY + 8)

      // Date
      const dateText = "June 12, 2025"
      const dateTextWidth = doc.getTextWidth(dateText)
      doc.text(dateText, pageWidth - margin - dateTextWidth, footerY + 12)
    }

    // Check if we need a new page
    const checkPageBreak = (neededSpace = 15) => {
      if (yPosition + neededSpace > pageHeight - footerHeight - 10) {
        addFooter()
        addNewPage()
        return true
      }
      return false
    }

    // Add text with proper wrapping and formatting
    const addFormattedText = (text, options = {}) => {
      const {
        fontSize = 10,
        fontStyle = "normal",
        color = colors.text,
        lineHeight = 5,
        leftIndent = 0,
        maxWidth = contentWidth,
        spaceAfter = 3,
      } = options

      doc.setFontSize(fontSize)
      doc.setFont("helvetica", fontStyle)
      doc.setTextColor(...color)

      // Calculate effective width
      const effectiveWidth = maxWidth - leftIndent
      const xPosition = margin + leftIndent

      // Split text to fit width
      const lines = doc.splitTextToSize(text, effectiveWidth)

      // Check if we need space for all lines
      checkPageBreak(lines.length * lineHeight + spaceAfter)

      // Add each line
      for (let i = 0; i < lines.length; i++) {
        if (checkPageBreak(lineHeight)) {
          // If we added a new page, recalculate position
        }
        doc.text(lines[i], xPosition, yPosition)
        yPosition += lineHeight
      }

      yPosition += spaceAfter
      return yPosition
    }

    // Add section header with professional styling
    const addSectionHeader = (text) => {
      checkPageBreak(25)

      // Add some space before section
      yPosition += 8

      // Section background
      doc.setFillColor(...colors.accent)
      doc.rect(margin - 2, yPosition - 8, contentWidth + 4, 16, "F")

      // Section border
      doc.setDrawColor(...colors.primary)
      doc.setLineWidth(0.8)
      doc.line(margin - 2, yPosition - 8, margin - 2, yPosition + 8)

      // Section text
      addFormattedText(text, {
        fontSize: 12,
        fontStyle: "bold",
        color: colors.primary,
        lineHeight: 6,
        spaceAfter: 8,
      })
    }

    // Add subsection header
    const addSubsectionHeader = (text) => {
      checkPageBreak(15)
      yPosition += 3

      addFormattedText(text, {
        fontSize: 11,
        fontStyle: "bold",
        color: colors.primary,
        leftIndent: 5,
        spaceAfter: 4,
      })
    }

    // Add bullet point
    const addBulletPoint = (text) => {
      const bulletText = "• " + text.substring(1).trim()
      addFormattedText(bulletText, {
        fontSize: 10,
        color: colors.textLight,
        leftIndent: 10,
        maxWidth: contentWidth - 10,
        lineHeight: 5,
        spaceAfter: 2,
      })
    }

    // Initialize first page
    addPageBackground()
    addHeader()

    // Process content
    const lines = content.split("\n")

    for (let i = 0; i < lines.length; i++) {
      const line = lines[i].trim()

      if (!line) {
        yPosition += 3
        continue
      }

      // Main title
      if ((line.includes("TERMS OF SERVICE") || line.includes("PRIVACY POLICY")) && i === 0) {
        checkPageBreak(30)
        yPosition += 10

        // Title background
        doc.setFillColor(...colors.primary)
        doc.rect(margin - 5, yPosition - 12, contentWidth + 10, 20, "F")

        addFormattedText(line, {
          fontSize: 16,
          fontStyle: "bold",
          color: colors.white,
          spaceAfter: 15,
        })
        continue
      }

      // Section headers (1. INTRODUCTION)
      if (/^\d+\.\s+[A-Z\s]+$/.test(line)) {
        addSectionHeader(line)
        continue
      }

      // Subsection headers (2.1 Personal Data)
      if (/^\d+\.\d+/.test(line)) {
        addSubsectionHeader(line)
        continue
      }

      // Bullet points
      if (line.startsWith("-")) {
        addBulletPoint(line)
        continue
      }

      // Company info and dates
      if (
        line.includes("Eclipse Growth Optimization Services") ||
        line.includes("Last Updated") ||
        line.includes("ego-services.com")
      ) {
        addFormattedText(line, {
          fontSize: 9,
          fontStyle: "italic",
          color: colors.textLight,
          spaceAfter: 5,
        })
        continue
      }

      // Regular paragraph text
      addFormattedText(line, {
        fontSize: 10,
        color: colors.text,
        lineHeight: 5,
        spaceAfter: 6,
      })
    }

    // Add footer to last page
    addFooter()

    // Add footers to all previous pages
    const totalPages = doc.internal.getNumberOfPages()
    for (let i = 1; i < totalPages; i++) {
      doc.setPage(i)
      currentPage = i
      addFooter()
    }

    // Save the PDF
    const filename = title.replace(/\s+/g, "-") + ".pdf"
    doc.save(filename)
  }

  script.onerror = () => {
    alert("Could not load PDF generation library. Please try again later.")
  }

  document.body.appendChild(script)
}

